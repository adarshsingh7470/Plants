# Define two complex numbers
complex_num1 = 3 + 4j
complex_num2 = 1 - 2j

# Addition
result_addition = complex_num1 + complex_num2
print("Addition:", result_addition)

# Subtraction
result_subtraction = complex_num1 - complex_num2
print("Subtraction:", result_subtraction)

# Multiplication
result_multiplication = complex_num1 * complex_num2
print("Multiplication:", result_multiplication)

# Division
result_division = complex_num1 / complex_num2
print("Division:", result_division)

# Absolute value (magnitude) of a complex number
magnitude = abs(complex_num1)
print("Magnitude of complex_num1:", magnitude)

# Real and Imaginary parts
real_part = complex_num1.real
imaginary_part = complex_num1.imag
print("Real Part of complex_num1:", real_part)
print("Imaginary Part of complex_num1:", imaginary_part)
